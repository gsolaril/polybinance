def parse_args(pairs: list[str]):
    params = dict[str, Any]()
    for pair in pairs:
        if "=" not in pair: raise ValueError(
            f"Parameter \"{pair}\" not in \"K=V\" form")
        key, value = pair.split("=", 1)
        try: params[key] = eval(value)
        except: params[key] = value
    return params
